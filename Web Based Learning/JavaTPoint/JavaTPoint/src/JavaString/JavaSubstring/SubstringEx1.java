public class SubstringEx1 {
    public static void main(String[] args) {
        String s = "Sachin Tendulkar";
        System.out.println(s.substring(6)); ///starting from 6 and finishes up to last one
        System.out.println(s.substring(0,6)); //starting at 0 index and finishes at 5th
    }
   
}
